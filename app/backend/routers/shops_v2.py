@router.delete("/batch")
async def delete_shops_v2s_batch(
    request: Shops_v2BatchDeleteRequest,
    current_user: UserResponse = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """Delete multiple shops_v2s by their IDs (requires ownership)"""
    logger.debug(f"Batch deleting {len(request.ids)} shops_v2s")
    
    service = Shops_v2Service(db)
    deleted_count = 0
    
    try:
        for item_id in request.ids:
            success = await service.delete(item_id, user_id=str(current_user.id))
            if success:
                deleted_count += 1
        
        logger.info(f"Batch deleted {deleted_count} shops_v2s successfully")
        return {"message": f"Successfully deleted {deleted_count} shops_v2s", "deleted_count": deleted_count}
    except Exception as e:
        await db.rollback()
        logger.error(f"Error in batch delete: {str(e)}", exc_info=True)
        raise HTTPException(status_code=500, detail=f"Batch delete failed: {str(e)}")


