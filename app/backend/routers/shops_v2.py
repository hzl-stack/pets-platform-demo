import json
import logging
from typing import List, Optional


from fastapi import APIRouter, Body, Depends, HTTPException, Query
from pydantic import BaseModel
from sqlalchemy.ext.asyncio import AsyncSession

from core.database import get_db
from services.shops_v2 import Shops_v2Service
from dependencies.auth import get_current_user
from schemas.auth import UserResponse

# Set up logging
logger = logging.getLogger(__name__)

router = APIRouter(prefix="/api/v1/entities/shops_v2", tags=["shops_v2"])


# ---------- Pydantic Schemas ----------
class Shops_v2Data(BaseModel):
    """Entity data schema (for create/update)"""
    shop_name: str
    description: str = None
    logo_url: str = None
    status: str = None
    average_rating: float = None
    created_at: str = None


class Shops_v2UpdateData(BaseModel):
    """Update entity data (partial updates allowed)"""
    shop_name: Optional[str] = None
    description: Optional[str] = None
    logo_url: Optional[str] = None
    status: Optional[str] = None
    average_rating: Optional[float] = None
    created_at: Optional[str] = None


class Shops_v2Response(BaseModel):
    """Entity response schema"""
    id: int
    user_id: str
    shop_name: str
    description: Optional[str] = None
    logo_url: Optional[str] = None
    status: Optional[str] = None
    average_rating: Optional[float] = None
    created_at: Optional[str] = None

    class Config:
        from_attributes = True


class Shops_v2ListResponse(BaseModel):
    """List response schema"""
    items: List[Shops_v2Response]
    total: int
    skip: int
    limit: int


class Shops_v2BatchCreateRequest(BaseModel):
    """Batch create request"""
    items: List[Shops_v2Data]


class Shops_v2BatchUpdateItem(BaseModel):
    """Batch update item"""
    id: int
    updates: Shops_v2UpdateData


class Shops_v2BatchUpdateRequest(BaseModel):
    """Batch update request"""
    items: List[Shops_v2BatchUpdateItem]


class Shops_v2BatchDeleteRequest(BaseModel):
    """Batch delete request"""
    ids: List[int]


# ---------- Routes ----------
@router.get("", response_model=Shops_v2ListResponse)
async def query_shops_v2s(
    query: str = Query(None, description="Query conditions (JSON string)"),
    sort: str = Query(None, description="Sort field (prefix with '-' for descending)"),
    skip: int = Query(0, ge=0, description="Number of records to skip"),
    limit: int = Query(20, ge=1, le=2000, description="Max number of records to return"),
    fields: str = Query(None, description="Comma-separated list of fields to return"),
    current_user: UserResponse = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    """Query shops_v2s with filtering, sorting, and pagination (user can only see their own records)"""
    logger.debug(f"Querying shops_v2s: query={query}, sort={sort}, skip={skip}, limit={limit}, fields={fields}")
    
    service = Shops_v2Service(db)
    try:
        # Parse query JSON if provided
        query_dict = None
        if query:
            try:
                query_dict = json.loads(query)
            except json.JSONDecodeError:
                raise HTTPException(status_code=400, detail="Invalid query JSON format")
        
        result = await service.get_list(
            skip=skip, 
            limit=limit,
            query_dict=query_dict,
            sort=sort,
            user_id=str(current_user.id),
        )
        logger.debug(f"Found {result['total']} shops_v2s")
        return result
    except HTTPException:
        raise
    except Exception as e:
        logger.error(f"Error querying shops_v2s: {str(e)}", exc_info=True)
        raise HTTPException(status_code=500, detail=f"Internal server error: {str(e)}")


